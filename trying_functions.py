def add_it(first_num, second_num):
    print(f"{first_num} + {second_num} = {second_num + first_num}")
    return first_num + second_num


def sub_it(fn, sn):
    result = fn - sn
    return result


my_sum = add_it(55, 123)
print(my_sum, "The result of the add_it function")

print(sub_it(my_sum, 7))


